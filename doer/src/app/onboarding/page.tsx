'use client'

import { useState, useEffect, useRef, Suspense } from 'react'
import { useRouter, useSearchParams } from 'next/navigation'
import { useSupabase } from '@/components/providers/supabase-provider'
import { Button } from '@/components/ui/Button'
import { Card, CardContent, CardHeader, CardTitle, CardDescription } from '@/components/ui/Card'
import { motion, AnimatePresence } from 'framer-motion'
import { Target, Calendar, ArrowRight, ArrowLeft, Loader2, AlertTriangle } from 'lucide-react'
import { detectSettingsConflicts, type SettingsConflict } from '@/lib/goal-analysis'
import { useSpeechRecognition } from '@/hooks/useSpeechRecognition'
import { VoiceInputButton } from '@/components/ui/VoiceInputButton'
import { useToast } from '@/components/ui/Toast'

function OnboardingContent() {
  const router = useRouter()
  const searchParams = useSearchParams()
  const { user } = useSupabase()
  const [goal, setGoal] = useState('')
  const [goalFromHomepage, setGoalFromHomepage] = useState(false)
  const [startDate, setStartDate] = useState(new Date().toISOString().split('T')[0])
  const [isGenerating, setIsGenerating] = useState(false)
  const [errorMessage, setErrorMessage] = useState<string | null>(null)
  const [hasAutoGenerated, setHasAutoGenerated] = useState(false)
  const [settingsConflict, setSettingsConflict] = useState<SettingsConflict | null>(null)
  const [userSettings, setUserSettings] = useState<{ 
    allow_weekends?: boolean
    workday_start_hour?: number
    workday_end_hour?: number
    lunch_start_hour?: number
    lunch_end_hour?: number
  } | null>(null)
  const hasFetchedWaitlistGoal = useRef(false)
  const { supabase } = useSupabase()
  const { addToast } = useToast()

  // Voice input integration
  const {
    isListening,
    transcript,
    error: speechError,
    isSupported: isSpeechSupported,
    startListening,
    stopListening,
    reset: resetSpeech,
  } = useSpeechRecognition({
    onResult: (finalTranscript) => {
      // Append to existing goal text or replace
      setGoal((prev) => {
        const newGoal = prev.trim() ? `${prev} ${finalTranscript}` : finalTranscript
        return newGoal
      })
      setErrorMessage(null)
    },
    onError: (error) => {
      addToast({
        type: 'error',
        title: 'Voice Input Error',
        description: error,
        duration: 5000,
      })
    },
    continuous: false, // Stop after each phrase
    interimResults: true, // Show real-time transcription
  })

  // Track text before starting voice input
  const textBeforeListeningRef = useRef<string>('')

  // Save current goal when starting to listen
  useEffect(() => {
    if (isListening && !textBeforeListeningRef.current) {
      textBeforeListeningRef.current = goal
    } else if (!isListening && textBeforeListeningRef.current) {
      textBeforeListeningRef.current = ''
    }
  }, [isListening])

  // Update goal with real-time transcripts while listening
  useEffect(() => {
    if (isListening) {
      const baseText = textBeforeListeningRef.current.trim()
      const fullText = transcript ? (baseText ? `${baseText} ${transcript}` : transcript) : baseText
      setGoal(fullText)
      setErrorMessage(null)
    }
  }, [transcript, isListening])

  // Handle microphone button click
  const handleMicClick = () => {
    if (isListening) {
      stopListening()
    } else {
      resetSpeech()
      startListening()
    }
  }

  /**
   * Goal Priority System:
   * 
   * Priority 1: URL parameter (from homepage GoalInput)
   *   - User explicitly enters a goal on homepage and signs up
   *   - Takes precedence over waitlist goal (user's explicit choice)
   * 
   * Priority 2: localStorage/sessionStorage (from homepage GoalInput)
   *   - Fallback if URL parameter not available
   *   - Also takes precedence over waitlist goal
   * 
   * Priority 3: Waitlist goal retrieval
   *   - Only used if no goal from homepage (Priorities 1 & 2)
   *   - For users who signed up to waitlist but didn't enter a new goal on homepage
   * 
   * Rationale: If a user explicitly enters a new goal on the homepage, it should
   * override their old waitlist goal, as it represents their current intent.
   */
  
  // Priority 1 & 2: Check URL parameter and localStorage/sessionStorage (from homepage)
  useEffect(() => {
    // Priority 1: URL parameter (from homepage GoalInput or signup redirect)
    const goalFromUrl = searchParams.get('goal')
    if (goalFromUrl) {
      setGoal(decodeURIComponent(goalFromUrl))
      setGoalFromHomepage(true)
      hasFetchedWaitlistGoal.current = true // Prevent waitlist fetch if goal from URL
      return
    }

    // Priority 2: localStorage/sessionStorage (from homepage GoalInput)
    const pendingGoal = localStorage.getItem('pendingGoal') || sessionStorage.getItem('pendingGoal')
    if (pendingGoal) {
      setGoal(pendingGoal)
      setGoalFromHomepage(true)
      localStorage.removeItem('pendingGoal')
      sessionStorage.removeItem('pendingGoal')
      hasFetchedWaitlistGoal.current = true // Prevent waitlist fetch if goal from storage
      return
    }
  }, [searchParams])

  // Priority 3: Waitlist goal retrieval (for users who were on waitlist)
  // Separate effect that only depends on user.email to ensure it runs when user becomes available
  useEffect(() => {
    // Only fetch if:
    // 1. User is authenticated
    // 2. Goal is currently empty (no goal from homepage)
    // 3. We haven't already fetched (to prevent duplicate calls)
    if (user?.email && !goal.trim() && !hasFetchedWaitlistGoal.current) {
      hasFetchedWaitlistGoal.current = true // Mark as fetched to prevent duplicate calls
      
      const fetchWaitlistGoal = async () => {
        try {
          console.log('[Onboarding] Fetching waitlist goal for email:', user.email)
          const response = await fetch('/api/waitlist/retrieve-goal')
          if (response.ok) {
            const data = await response.json()
            console.log('[Onboarding] Waitlist goal response:', data)
            if (data.success && data.goal) {
              console.log('[Onboarding] Setting goal from waitlist:', data.goal)
              setGoal(data.goal)
              setGoalFromHomepage(true)
            } else {
              console.log('[Onboarding] No goal found in waitlist for this email')
            }
          } else {
            const errorData = await response.json().catch(() => ({}))
            console.error('[Onboarding] Failed to retrieve waitlist goal - response not ok:', response.status, errorData)
          }
        } catch (error) {
          // Silently fail - user can still enter goal manually
          console.error('[Onboarding] Failed to retrieve waitlist goal:', error)
        }
      }
      fetchWaitlistGoal()
    }
  }, [user?.email, goal])

  // Fetch user settings
  useEffect(() => {
    const fetchUserSettings = async () => {
      if (!user || !supabase) return
      
      try {
        const { data: settings } = await supabase
          .from('user_settings')
          .select('preferences')
          .eq('user_id', user.id)
          .maybeSingle()
        
        const prefs = (settings?.preferences as any) ?? {}
        const workdayPrefs = prefs.workday || {}
        setUserSettings({
          allow_weekends: Boolean(workdayPrefs.allow_weekends ?? prefs.allow_weekends ?? false),
          workday_start_hour: workdayPrefs.workday_start_hour ?? prefs.workday_start_hour,
          workday_end_hour: workdayPrefs.workday_end_hour ?? prefs.workday_end_hour,
          lunch_start_hour: workdayPrefs.lunch_start_hour ?? prefs.lunch_start_hour,
          lunch_end_hour: workdayPrefs.lunch_end_hour ?? prefs.lunch_end_hour,
        })
      } catch (error) {
        console.error('Failed to fetch user settings:', error)
        // Default to allowing weekends if we can't fetch settings
        setUserSettings({ allow_weekends: true })
      }
    }
    
    fetchUserSettings()
  }, [user, supabase])

  // Auto-generate plan when user is authenticated and goal is set (from homepage)
  useEffect(() => {
    if (user && goal.trim() && !isGenerating && !hasAutoGenerated && goalFromHomepage && userSettings !== null) {
      const timer = setTimeout(() => {
        setHasAutoGenerated(true)
        handleGeneratePlan()
      }, 1000)
      return () => clearTimeout(timer)
    }
  }, [goal, goalFromHomepage, hasAutoGenerated, isGenerating, user, userSettings])


  if (!user) {
    return (
      <div className="min-h-screen bg-[#0a0a0a] flex items-center justify-center">
        <div className="text-center">
          <p className="text-[#d7d2cb]">Please log in to continue</p>
          <button
            onClick={() => router.push('/login')}
            className="mt-4 px-4 py-2 bg-[var(--primary)] text-white rounded-md hover:bg-[var(--primary)]/90"
          >
            Go to Login
          </button>
        </div>
      </div>
    )
  }

  const handleGeneratePlan = async () => {
    if (!goal.trim() || !startDate) {
      setErrorMessage('Please fill in all fields before generating your plan.')
      return
    }

    // Check for conflicts before generating plan
    if (userSettings !== null) {
      const conflicts = detectSettingsConflicts(goal.trim(), undefined, userSettings)
      if (conflicts.length > 0) {
        setSettingsConflict(conflicts[0])
        return
      }
    }

    // Skip initial clarifications - go directly to plan generation
    await generatePlan({})
  }

  const handleResolveConflict = () => {
    if (settingsConflict) {
      // Redirect to settings page with the appropriate tab
      router.push(`/settings?tab=${settingsConflict.settingsTab}`)
    }
  }

  const generatePlan = async (clarifications: Record<string, string>) => {
    setIsGenerating(true)
    setErrorMessage(null)

    try {
      const response = await fetch('/api/plans/generate', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          goal_text: goal.trim(),
          start_date: startDate,
          clarifications: Object.keys(clarifications).length > 0 ? clarifications : undefined,
          timezone_offset: new Date().getTimezoneOffset(), // Send timezone offset in minutes
        }),
      })

      const data = await response.json()

      if (!response.ok) {
        // Check if this is a settings conflict error
        if (data?.error === 'SETTINGS_CONFLICT' && data?.conflict) {
          setSettingsConflict(data.conflict)
          return
        }
        // Check if this is a usage limit exceeded error
        if (data?.error === 'USAGE_LIMIT_EXCEEDED' || data?.message?.includes('USAGE_LIMIT_EXCEEDED') || data?.message?.includes("plan's limit")) {
          setErrorMessage("You've reached your plan's limit for this feature. Please upgrade your plan or wait for the next billing cycle.")
        } else {
          const message =
            data?.message ||
            data?.error ||
            'Failed to generate plan.'
          setErrorMessage(message)
        }
        return
      }

      if (data.success && data.plan?.id) {
        const tasksArray = Array.isArray(data.tasks) ? data.tasks : Array.isArray(data.tasks?.items) ? data.tasks.items : []
        sessionStorage.setItem('generatedPlan', JSON.stringify({
          plan: data.plan,
          tasks: tasksArray,
        }))
        router.push('/onboarding/review')
      } else {
        throw new Error('Plan creation failed - no plan ID returned')
      }
    } catch (error) {
      console.error('Error generating plan:', error)
      setErrorMessage(
        error instanceof Error ? error.message : 'Failed to generate plan. Please try again.'
      )
    } finally {
      setIsGenerating(false)
    }
  }


  return (
    <div className="min-h-screen bg-[#0a0a0a] flex items-center justify-center py-12 px-4">
      <div className="w-full max-w-2xl">
        <motion.div
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          transition={{ duration: 0.5, ease: 'easeOut' }}
        >
              <motion.div
                initial={{ opacity: 0, y: -20 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ duration: 0.5, delay: 0.1, ease: 'easeOut' }}
                className="text-center mb-8"
              >
                <h1 className="text-4xl font-bold text-[#d7d2cb] mb-4">
                  Let's Create Your Plan
                </h1>
                <p className="text-xl text-[#d7d2cb]/70">
                  Tell us your goal and we'll generate a personalized action plan
                </p>
              </motion.div>

              <motion.div
                initial={{ opacity: 0, y: 20 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ duration: 0.5, delay: 0.2, ease: 'easeOut' }}
              >
                <Card className="bg-white/5 border-white/10 relative overflow-hidden">
                <CardHeader>
                  <AnimatePresence mode="wait">
                    {settingsConflict && (
                      <motion.div
                        initial={{ opacity: 0, y: -10, scale: 0.95 }}
                        animate={{ opacity: 1, y: 0, scale: 1 }}
                        exit={{ opacity: 0, y: -10, scale: 0.95 }}
                        transition={{ duration: 0.3, ease: 'easeOut' }}
                        className="mb-4 rounded-lg border border-orange-500/40 bg-orange-500/10 px-4 py-3 text-left"
                      >
                        <div className="flex items-start gap-3">
                          <motion.div
                            initial={{ scale: 0, rotate: -180 }}
                            animate={{ scale: 1, rotate: 0 }}
                            transition={{ duration: 0.4, delay: 0.1, type: 'spring', stiffness: 200 }}
                          >
                            <AlertTriangle className="w-5 h-5 text-orange-400 mt-0.5 flex-shrink-0" />
                          </motion.div>
                          <div className="flex-1">
                            <motion.p
                              initial={{ opacity: 0, x: -10 }}
                              animate={{ opacity: 1, x: 0 }}
                              transition={{ duration: 0.3, delay: 0.15 }}
                              className="text-sm font-medium text-orange-200 mb-2"
                            >
                              Settings Conflict Detected
                            </motion.p>
                            <motion.p
                              initial={{ opacity: 0, x: -10 }}
                              animate={{ opacity: 1, x: 0 }}
                              transition={{ duration: 0.3, delay: 0.2 }}
                              className="text-xs text-orange-200/80 mb-3"
                            >
                              {settingsConflict.description}
                            </motion.p>
                            <motion.div
                              initial={{ opacity: 0, x: -10 }}
                              animate={{ opacity: 1, x: 0 }}
                              transition={{ duration: 0.3, delay: 0.25 }}
                              className="flex flex-col gap-2"
                            >
                              <motion.a
                                href={`/settings?tab=${settingsConflict.settingsTab}`}
                                onClick={(e) => {
                                  e.preventDefault()
                                  handleResolveConflict()
                                }}
                                whileHover={{ scale: 1.02 }}
                                whileTap={{ scale: 0.98 }}
                                className="text-xs text-orange-300 hover:text-orange-200 underline transition-colors cursor-pointer"
                              >
                                Update settings →
                              </motion.a>
                              <motion.p
                                initial={{ opacity: 0 }}
                                animate={{ opacity: 1 }}
                                transition={{ duration: 0.3, delay: 0.3 }}
                                className="text-xs text-orange-200/60"
                              >
                                {settingsConflict.alternativeText}
                              </motion.p>
                            </motion.div>
                          </div>
                        </div>
                      </motion.div>
                    )}
                  </AnimatePresence>
                  {errorMessage && !settingsConflict && (
                    <div className="mb-4 rounded-lg border border-red-500/40 bg-red-500/10 px-4 py-3 text-left">
                      {errorMessage.includes("plan's limit") ? (
                        <div className="flex flex-col gap-2">
                          <p className="text-sm font-medium text-red-200">
                            Feature limit reached
                          </p>
                          <p className="text-xs text-red-200/80">
                            {errorMessage}
                          </p>
                          <a
                            href="/pricing"
                            className="text-xs underline mt-1 text-red-200/80 hover:text-red-200 transition-colors"
                          >
                            View pricing plans →
                          </a>
                        </div>
                      ) : (
                        <p className="text-sm text-red-200">{errorMessage}</p>
                      )}
                    </div>
                  )}
                  <div className="flex items-center gap-4 mb-2">
                    <div className="w-12 h-12 rounded-full bg-[#ff7f00]/20 flex items-center justify-center">
                      <Target className="w-6 h-6 text-[#ff7f00]" />
                    </div>
                    <div>
                      <CardTitle className="text-2xl text-[#d7d2cb]">
                        Describe Your Goal
                      </CardTitle>
                      <CardDescription className="text-[#d7d2cb]/70 mt-1">
                        What do you want to achieve?
                      </CardDescription>
                    </div>
                  </div>
                </CardHeader>
                <CardContent className="space-y-6">
                  <div className="relative">
                    <textarea
                      id="goal"
                      value={goal}
                      onChange={(e) => setGoal(e.target.value)}
                      placeholder="e.g., Learn to play guitar, Start a blog, Get in shape..."
                      className={`w-full px-4 py-4 ${isSpeechSupported ? 'pr-14' : 'pr-4'} text-lg bg-white/5 border border-white/10 rounded-xl text-[#d7d2cb] placeholder-[#d7d2cb]/40 focus:outline-none focus:border-[#ff7f00] focus:ring-2 focus:ring-[#ff7f00]/20 transition-all resize-none`}
                      rows={4}
                    />
                    
                    {/* Voice input button */}
                    {isSpeechSupported && (
                      <div className="absolute right-2 bottom-2 z-10">
                        <VoiceInputButton
                          isListening={isListening}
                          isSupported={isSpeechSupported}
                          onClick={handleMicClick}
                          disabled={isGenerating}
                          size="sm"
                          error={speechError}
                        />
                      </div>
                    )}

                    {/* Real-time listening indicator */}
                    {isListening && (
                      <div className="absolute top-2 left-2 flex items-center gap-2 px-3 py-1 bg-red-500/20 border border-red-500/50 rounded-lg text-xs text-red-400 z-20">
                        <Loader2 className="w-3 h-3 animate-spin" />
                        <span>Listening...</span>
                      </div>
                    )}
                  </div>

                  {/* Show speech errors */}
                  {speechError && !errorMessage && (
                    <p className="text-sm text-yellow-400">{speechError}</p>
                  )}

                  <div>
                    <div className="flex items-center gap-2 mb-3">
                      <Calendar className="w-4 h-4 text-[#d7d2cb]/70" />
                      <label htmlFor="startDate" className="text-sm font-medium text-[#d7d2cb]/70">
                        Start Date
                      </label>
                    </div>
                    <input
                      type="date"
                      id="startDate"
                      value={startDate}
                      onChange={(e) => setStartDate(e.target.value)}
                      className="w-full px-4 py-4 text-lg bg-white/5 border border-white/10 rounded-xl text-[#d7d2cb] focus:outline-none focus:border-[#ff7f00] focus:ring-2 focus:ring-[#ff7f00]/20 transition-all"
                    />
                  </div>

                  <div className="pt-6 border-t border-white/10">
                    <Button
                      onClick={handleGeneratePlan}
                      disabled={!goal.trim() || !startDate || isGenerating}
                      className="w-full flex items-center justify-center gap-2"
                      variant="primary"
                      size="lg"
                    >
                      {isGenerating ? (
                        <Loader2 className="w-5 h-5 animate-spin" />
                      ) : (
                        <>
                          Generate My Plan
                          <ArrowRight className="w-4 h-4" />
                        </>
                      )}
                    </Button>
                  </div>
                </CardContent>
              </Card>
              </motion.div>

              <motion.div
                initial={{ opacity: 0 }}
                animate={{ opacity: 1 }}
                transition={{ duration: 0.4, delay: 0.4, ease: 'easeOut' }}
                className="mt-6 flex justify-center"
              >
                <Button
                  onClick={() => router.push('/dashboard')}
                  variant="outline"
                  size="sm"
                  className="flex items-center gap-2"
                >
                  <ArrowLeft className="w-4 h-4" />
                  Return to Dashboard
                </Button>
              </motion.div>
            </motion.div>
      </div>
    </div>
  )
}

export default function OnboardingPage() {
  return (
    <Suspense fallback={
      <div className="min-h-screen bg-[#0a0a0a] flex items-center justify-center">
        <div className="w-8 h-8 border-4 border-[#ff7f00] border-t-transparent rounded-full animate-spin" />
      </div>
    }>
      <OnboardingContent />
    </Suspense>
  )
}